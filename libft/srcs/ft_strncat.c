#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t count)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (*(s1 + j) != 0)
		j++;
	while (*(s2 + i) != 0 && count)
	{
		*(s1 + j + i) = *(s2 + i);
		i++;
		count--;
	}
	*(s1 + j + i) = '\0';
	return (s1);
}
